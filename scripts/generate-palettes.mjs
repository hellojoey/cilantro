// Generates src/theme/palettes.css from src/theme/palettes.js.
// palettes.js is the single source of truth; this emits the CSS custom-property
// blocks the cascade needs so a palette survives a dark-mode toggle without JS
// re-applying inline styles. Run `npm run theme:build` after editing palettes.js.
import { writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';
import { PALETTES, CONSTANTS, DEFAULT_FAMILY, hexToTriplet } from '../src/theme/palettes.js';

const here = dirname(fileURLToPath(import.meta.url));
const out = join(here, '..', 'src', 'theme', 'palettes.css');

const block = (selector, palette) => {
  const lines = Object.entries(palette)
    .map(([token, hex]) => `  --c-${token}: ${hexToTriplet(hex)};`)
    .join('\n');
  return `${selector} {\n${lines}\n}`;
};

const chunks = [
  '/* GENERATED by scripts/generate-palettes.mjs — do not edit.',
  '   Source of truth: src/theme/palettes.js. Run `npm run theme:build`. */',
  '',
];

// Light first, then dark. `:root[data-palette=x]` and `.dark[data-palette=x]`
// have identical specificity (0,2,0), so source order is what makes dark win.
for (const mode of ['light', 'dark']) {
  chunks.push(`/* ── ${mode} ── */`);
  // Vibe-independent tokens ride on the bare :root / .dark selector.
  chunks.push(block(mode === 'dark' ? '.dark' : ':root', CONSTANTS[mode]));
  for (const [family, modes] of Object.entries(PALETTES)) {
    const selectors = [`${mode === 'dark' ? '.dark' : ':root'}[data-palette="${family}"]`];
    // The default family also answers to a bare :root / .dark, so first paint is
    // themed before React sets data-palette.
    if (family === DEFAULT_FAMILY) selectors.unshift(mode === 'dark' ? '.dark' : ':root');
    chunks.push(block(selectors.join(',\n'), modes[mode]));
  }
  chunks.push('');
}

writeFileSync(out, chunks.join('\n'));
console.log(`wrote ${out} — ${Object.keys(PALETTES).length} families x2 modes`);
